import math


def area(r):
    """
    Принимает значение радиуса круга r.
    Возвращает площадь круга с данным радиусом
    """

    return math.pi * r * r


def perimeter(r):
    """
    Принимает значение Периметра круга
    Возвращает Периметр данного круга с заданным радиусом
    """

    return 2 * math.pi * r

